						<center><h2>Marketing</h2></center>
						<p>Once we began to get more artwork concepts and finished images out, marketing was the next stage. Creating posts which users would find engaging whilst also building up interest before launch. After gaining roughly 200 followers in each; Twitter, Facebook, DeviantArt. We begain to launch our Steam Greenlight campaign. This took a little longer than planned, however after roughly a month, Runeous was Greenlit.</p>
						<p><a href="https://twitter.com/NovelTeaGames">Novel Tea Games'</a> Twitter still holds over 500 followers who I post to from time to time, helping other developers with marketing their Visual Novel games.</p>
						<center><h2>Release</h2></center>
						<p>With Runeous being a volunteer project and 'hobby' from the start, we continued with plans to release the Visual Novel for free. While the game had been in planning since 2014, actual development took 1 year, starting in 2015 and releasing May 2016 on <a href="http://store.steampowered.com/app/485800/">Steam</a>.</p>
						<center><h2>The 'Video Novel'</h2></center>
						<p>As Runeous was put up for free release, we also decided to create a 'Video Novel' on Youtube. This was a conversion of the assets into video form with Voice Actors playing the roles of all key characters and NPCs. Various sound effects were also added at this stage which weren't available in the Visual Novel. This however meant there was no Multiple Choice options and therefor follows the path I originally intended for the story to follow.</p>
